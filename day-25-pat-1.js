let keys = []
let locks = []

for (const element of elements) {
    if (element.startsWith("#")) {
        locks.push(element)
    } else {
        keys.push(element)
    }
}

function decode(array) {
    return array.map(element => {
        const pins = []
        const rows = element.split("\n").slice(1, -1)
        for (let i = 0; i < 5; i++) {
            let count = 0
            rows.forEach(row => {
                if (row[i] === "#") {
                    count++
                }
            })
            pins.push(count)
        }
        return pins
    })
}

